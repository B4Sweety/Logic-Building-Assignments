#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Accept
// Description   : Used to print that number of * on screen
// Input         : -
// Output        : *
// Date          : 11/05/2025
// Author        : Sweety Prataprao Bankar
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Accept(int iNo)
{
	int iCnt = 0;
	
	for(iCnt =1; iCnt<=iNo; iCnt++)
	{
		printf("*\n");
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//Accept one number from user and print that number of * on screen
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	iValue = 5;
	
	Accept(iValue);
	
	return 0;
}

