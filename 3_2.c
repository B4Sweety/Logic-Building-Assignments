void DisplayFactor(int iNo)
{
	int iCnt = 0;
	
	if(iNo <= 0)
	{
		iNo=-iNo;
	}
	
	for(iCnt=1; iCnt < iNo; iCnt++)
	{
		if((iNo%iCnt) == 0)
		{
			printf("%d\t",iCnt);
		}
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//Accept one number from user and print Factors of that number
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	
	printf("Enter number\n");
	scanf("%d",&iValue);
	
	DisplayFactor(iValue);
	
	return 0;
}

