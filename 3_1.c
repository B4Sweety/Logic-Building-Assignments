void PrintEven(int iNo)
{
	int iCnt =0,iEven=1;
	
	if(iNo <= 0)
	{
		return;
	}
	
	for(iCnt=1; iEven <= iNo; iCnt++)
	{
			if(iCnt%2==0)
			{
				iEven = iEven+1;
				printf("%d\n",iCnt);
			}
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//Accept one number from user and print that Number of even number on screen
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	
	printf("Enter number\n");
	scanf("%d",&iValue);
	
	PrintEven(iValue);
	
	return 0;
}

