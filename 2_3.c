#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Display
// Description   : Used to print Hello otherwise Demo
// Input         : integer
// Output        : *
// Date          : 11/05/2025
