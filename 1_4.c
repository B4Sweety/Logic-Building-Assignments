#include<stdio.h>

typedef int BOOL;
#define TRUE 1
#define FALSE 0

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function Name : Check
// Description   : Used to check whether number is divisible by 5 or not
// Input         : Integer
// Output        : Boolean
// Date          : 11/05/2025
// Author        : Sweety Prataprao Bankar
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

BOOL Check(int iNo)
{
	if((iNo % 5) == 0)
	{
		return TRUE;
	}
	else
	{
		return FALSE;
	}
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
//Accept number and check whether it is divisible by 5 or not
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
	int iValue = 0;
	BOOL bRet = FALSE;
	
	printf("Enter number\n ");
	scanf("%d",&iValue);
	
	bRet = Check(iValue);
	
	if(bRet == TRUE)
	{
		printf("Number is divisible by 5\n");
	}
	else
	{
		printf("Number is not divisible by 5\n");
	}
}


